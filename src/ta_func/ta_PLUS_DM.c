/* Private function, not in public API. */
static void TA_PLUS_DM_StepInternal( struct TA_PLUS_DM_Stream *sp, double inHigh, double inLow, double *outReal )
{
   if( sp->optInTimePeriod <= 1 )
   {
      sp->tempReal = inHigh;
      sp->diffP = sp->tempReal - sp->prevHigh;
      /* Plus Delta */
      sp->prevHigh = sp->tempReal;
      sp->tempReal = inLow;
      sp->diffM = sp->prevLow - sp->tempReal;
      /* Minus Delta */
      sp->prevLow = sp->tempReal;
      if( sp->diffP > 0 && sp->diffP > sp->diffM )
      {
         /* Case 1 and 3: +DM=diffP,-DM=0 */
         *outReal= sp->diffP;
      } else 
      {
         *outReal= 0;
      }
   }
   else
   {
      sp->tempReal = inHigh;
      sp->diffP = sp->tempReal - sp->prevHigh;
      /* Plus Delta */
      sp->prevHigh = sp->tempReal;
      sp->tempReal = inLow;
      sp->diffM = sp->prevLow - sp->tempReal;
      /* Minus Delta */
      sp->prevLow = sp->tempReal;
      if( sp->diffP > 0 && sp->diffP > sp->diffM )
      {
         /* Case 1 and 3: +DM=diffP,-DM=0 */
         sp->prevPlusDM = sp->prevPlusDM - sp->prevPlusDM / sp->optInTimePeriod + sp->diffP;
      } else 
      {
         /* Case 2,4,5 and 7 */
         sp->prevPlusDM = sp->prevPlusDM - sp->prevPlusDM / sp->optInTimePeriod;
      }
      *outReal= sp->prevPlusDM;
   }
}

