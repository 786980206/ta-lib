/**** Streaming API *****/

struct TA_NATR_Stream {
   int optInTimePeriod;
   double prevATR;
   double tempValue;
   double val3;
   double lag1_inClose;
};

/* Private function, not in public API. */
static void TA_NATR_StepInternal( struct TA_NATR_Stream *sp, double inHigh, double inLow, double inClose, double *outReal )
{
   double val2;
   double greatest;
   double tempCY;
   double tempLT;
   double tempHT;

   /* Find the greatest of the 3 values. */
   tempLT = inLow;
   tempHT = inHigh;
   tempCY = sp->lag1_inClose;
   greatest = tempHT - tempLT;
   /* val1 */
   val2 = fabs(tempCY - tempHT);
   if( val2 > greatest )
   {
      greatest = val2;
   }
   sp->val3 = fabs(tempCY - tempLT);
   if( sp->val3 > greatest )
   {
      greatest = sp->val3;
   }
   sp->prevATR *= sp->optInTimePeriod - 1;
   sp->prevATR += greatest;
   sp->prevATR /= sp->optInTimePeriod;
   if( sp->optInTimePeriod <= 1 )
   {
      /* No smoothing: emit the raw True Range (unnormalized). */
      *outReal= sp->prevATR;
   } else 
   {
      sp->tempValue = inClose;
      if( !TA_IS_ZERO(sp->tempValue) )
      {
         *outReal= sp->prevATR / sp->tempValue * 100.0;
      } else 
      {
         *outReal= 0.0;
      }
   }
   sp->lag1_inClose = inClose;
}

