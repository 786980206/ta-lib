static void TA_ATR_StreamStep( struct TA_ATR_Stream *sp, double inHigh, double inLow, double inClose, double *outReal )
{
   double val2;
   double greatest;
   double tempCY;
   double tempLT;
   double tempHT;

   /* Find the greatest of the 3 values. */
   tempLT = inLow;
   tempHT = inHigh;
   tempCY = sp->lag1_inClose;
   greatest = tempHT - tempLT;
   /* val1 */
   val2 = fabs(tempCY - tempHT);
   if( val2 > greatest )
   {
      greatest = val2;
   }
   sp->val3 = fabs(tempCY - tempLT);
   if( sp->val3 > greatest )
   {
      greatest = sp->val3;
   }
   sp->prevATR *= sp->optInTimePeriod - 1;
   sp->prevATR += greatest;
   sp->prevATR /= sp->optInTimePeriod;
   *outReal= sp->prevATR;
   sp->lag1_inClose = inClose;
}

