      return -1;
   if( (int)optInFastK_Period == (int)0x80000000 )
      optInFastK_Period = 5;
   else if( (int)optInFastK_Period < 1 || (int)optInFastK_Period > 100000 )
      return -1;
   if( (int)optInFastD_Period == (int)0x80000000 )
      optInFastD_Period = 3;
