/**** Streaming API *****/

struct TA_MINUS_DI_Stream {
   int optInTimePeriod;
   double prevHigh;
   double prevLow;
   double prevClose;
   double tempReal;
   double diffP;
   double diffM;
   double prevMinusDM;
   double prevTR;
};

/* Private function, not in public API. */
static void TA_MINUS_DI_StepInternal( struct TA_MINUS_DI_Stream *sp, double inHigh, double inLow, double inClose, double *outReal )
{
   if( sp->optInTimePeriod <= 1 )
   {
      sp->tempReal = inHigh;
      sp->diffP = sp->tempReal - sp->prevHigh;
      /* Plus Delta */
      sp->prevHigh = sp->tempReal;
      sp->tempReal = inLow;
      sp->diffM = sp->prevLow - sp->tempReal;
      /* Minus Delta */
      sp->prevLow = sp->tempReal;
      if( sp->diffM > 0 && sp->diffP < sp->diffM )
      {
         /* Case 2 and 4: +DM=0,-DM=diffM */
         double _true_range_0;
         double range_0 = sp->prevHigh - sp->prevLow;
         double tmp_0 = fabs(sp->prevHigh - sp->prevClose);
         if( tmp_0 > range_0 )
         {
            range_0 = tmp_0;
         }
         tmp_0 = fabs(sp->prevLow - sp->prevClose);
         if( tmp_0 > range_0 )
         {
            range_0 = tmp_0;
         }
         _true_range_0 = range_0;
         sp->tempReal = _true_range_0;
         if( TA_IS_ZERO(sp->tempReal) )
         {
            *outReal= (double)0.0;
         } else 
         {
            *outReal= sp->diffM / sp->tempReal;
         }
      } else 
      {
         *outReal= (double)0.0;
      }
      sp->prevClose = inClose;
   }
   else
   {
      /* Calculate the prevMinusDM */
      sp->tempReal = inHigh;
      sp->diffP = sp->tempReal - sp->prevHigh;
      /* Plus Delta */
      sp->prevHigh = sp->tempReal;
      sp->tempReal = inLow;
      sp->diffM = sp->prevLow - sp->tempReal;
      /* Minus Delta */
      sp->prevLow = sp->tempReal;
      if( sp->diffM > 0 && sp->diffP < sp->diffM )
      {
         /* Case 2 and 4: +DM=0,-DM=diffM */
         sp->prevMinusDM = sp->prevMinusDM - sp->prevMinusDM / sp->optInTimePeriod + sp->diffM;
      } else 
      {
         /* Case 1,3,5 and 7 */
         sp->prevMinusDM = sp->prevMinusDM - sp->prevMinusDM / sp->optInTimePeriod;
      }
      /* Calculate the prevTR */
      double _true_range_1;
      double range_1 = sp->prevHigh - sp->prevLow;
      double tmp_1 = fabs(sp->prevHigh - sp->prevClose);
      if( tmp_1 > range_1 )
      {
         range_1 = tmp_1;
      }
      tmp_1 = fabs(sp->prevLow - sp->prevClose);
      if( tmp_1 > range_1 )
      {
         range_1 = tmp_1;
      }
      _true_range_1 = range_1;
      sp->tempReal = _true_range_1;
      sp->prevTR = sp->prevTR - sp->prevTR / sp->optInTimePeriod + sp->tempReal;
      sp->prevClose = inClose;
      /* Calculate the DI. The value is rounded (see Wilder book). */
      if( !TA_IS_ZERO(sp->prevTR) )
      {
         *outReal= (100.0 * (sp->prevMinusDM / sp->prevTR));
      } else 
      {
         *outReal= 0.0;
      }
   }
}

