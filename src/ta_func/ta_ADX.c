      memset( sp, 0, sizeof(*sp) );
      sp->optInTimePeriod = optInTimePeriod;
      sp->prevHigh = prevHigh;
      sp->prevLow = prevLow;
      sp->prevClose = prevClose;
      sp->prevMinusDM = prevMinusDM;
