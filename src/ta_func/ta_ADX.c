/**** Streaming API *****/

struct TA_ADX_Stream {
   int optInTimePeriod;
   double prevHigh;
   double prevLow;
   double prevClose;
   double prevMinusDM;
   double prevPlusDM;
   double prevTR;
   double tempReal;
   double diffP;
   double diffM;
   double minusDI;
   double plusDI;
   double prevADX;
};

/* Private function, not in public API. */
static void TA_ADX_StepInternal( struct TA_ADX_Stream *sp, double inHigh, double inLow, double inClose, double *outReal )
{
   /* Calculate the prevMinusDM and prevPlusDM */
   sp->tempReal = inHigh;
   sp->diffP = sp->tempReal - sp->prevHigh;
   /* Plus Delta */
   sp->prevHigh = sp->tempReal;
   sp->tempReal = inLow;
   sp->diffM = sp->prevLow - sp->tempReal;
   /* Minus Delta */
   sp->prevLow = sp->tempReal;
   sp->prevMinusDM -= sp->prevMinusDM / sp->optInTimePeriod;
   sp->prevPlusDM -= sp->prevPlusDM / sp->optInTimePeriod;
   if( sp->diffM > 0 && sp->diffP < sp->diffM )
   {
      /* Case 2 and 4: +DM=0,-DM=diffM */
      sp->prevMinusDM += sp->diffM;
   } else if( sp->diffP > 0 && sp->diffP > sp->diffM )
   {
      /* Case 1 and 3: +DM=diffP,-DM=0 */
      sp->prevPlusDM += sp->diffP;
   }
   /* Calculate the prevTR */
   double _true_range_0;
   double range_0 = sp->prevHigh - sp->prevLow;
   double tmp_0 = fabs(sp->prevHigh - sp->prevClose);
   if( tmp_0 > range_0 )
   {
      range_0 = tmp_0;
   }
   tmp_0 = fabs(sp->prevLow - sp->prevClose);
   if( tmp_0 > range_0 )
   {
      range_0 = tmp_0;
   }
   _true_range_0 = range_0;
   sp->tempReal = _true_range_0;
   sp->prevTR = sp->prevTR - sp->prevTR / sp->optInTimePeriod + sp->tempReal;
   sp->prevClose = inClose;
   if( !TA_IS_ZERO(sp->prevTR) )
   {
      /* Calculate the DX. The value is rounded (see Wilder book). */
      sp->minusDI = (100.0 * (sp->prevMinusDM / sp->prevTR));
      sp->plusDI = (100.0 * (sp->prevPlusDM / sp->prevTR));
      sp->tempReal = sp->minusDI + sp->plusDI;
      if( !TA_IS_ZERO(sp->tempReal) )
      {
         sp->tempReal = (100.0 * (fabs(sp->minusDI - sp->plusDI) / sp->tempReal));
         /* Calculate the ADX */
         sp->prevADX = ((sp->prevADX * (sp->optInTimePeriod - 1) + sp->tempReal) / sp->optInTimePeriod);
      }
   }
   /* Output the ADX */
   *outReal= sp->prevADX;
}

/* Private function, not in public API. */
TA_RetCode TA_ADX_OpenInternal( struct TA_ADX_Stream **stream, const double inHigh[], const double inLow[], const double inClose[], int startIdx, int historyLen, int optInTimePeriod, double *outReal )
{
   struct TA_ADX_Stream *sp;
   int endIdx;
   int dummyBegIdx;
   int dummyNBElement;
   double lastValue_outReal;

   if( !stream ) return TA_BAD_PARAM;
   *stream = NULL;
   if( !inHigh || !inLow || !inClose || !outReal ) return TA_BAD_PARAM;
   if( historyLen < 1 ) return TA_BAD_PARAM;
   if( (int)optInTimePeriod == (int)0x80000000 )
      optInTimePeriod = 14;
   else if( (int)optInTimePeriod < 2 || (int)optInTimePeriod > 100000 )
      return TA_BAD_PARAM;

   endIdx = historyLen - 1;
   dummyBegIdx = 0;
   dummyNBElement = 0;
   lastValue_outReal = 0.0;
   (void)startIdx; (void)dummyBegIdx; (void)dummyNBElement;

   {
      int today;
      int lookbackTotal;
      int outIdx;
      double prevHigh = 0.0;
      double prevLow = 0.0;
      double prevClose = 0.0;
      double prevMinusDM = 0.0;
      double prevPlusDM = 0.0;
      double prevTR = 0.0;
      double tempReal = 0.0;
      double tempReal2;
      double diffP = 0.0;
      double diffM = 0.0;
      double minusDI = 0.0;
      double plusDI = 0.0;
      double sumDX;
      double prevADX = 0.0;
      int i;
      /*
       * The DM1 (one period) is base on the largest part of
       * today's range that is outside of yesterdays range.
       *
       * The following 7 cases explain how the +DM and -DM are
       * calculated on one period:
       *
       * Case 1:                       Case 2:
       *    C|                        A|
       *     |                         | C|
       *     | +DM1 = (C-A)           B|  | +DM1 = 0
       *     | -DM1 = 0                   | -DM1 = (B-D)
       * A|  |                           D|
       *  | D|
       * B|
       *
       * Case 3:                       Case 4:
       *    C|                           C|
       *     |                        A|  |
       *     | +DM1 = (C-A)            |  | +DM1 = 0
       *     | -DM1 = 0               B|  | -DM1 = (B-D)
       * A|  |                            |
       *  |  |                           D|
       * B|  |
       *    D|
       *
       * Case 5:                      Case 6:
       * A|                           A| C|
       *  | C| +DM1 = 0                |  |  +DM1 = 0
       *  |  | -DM1 = 0                |  |  -DM1 = 0
       *  | D|                         |  |
       * B|                           B| D|
       *
       *
       * Case 7:
       *
       *    C|
       * A|  |
       *  |  | +DM=0
       * B|  | -DM=0
       *    D|
       *
       * In case 3 and 4, the rule is that the smallest delta between
       * (C-A) and (B-D) determine which of +DM or -DM is zero.
       *
       * In case 7, (C-A) and (B-D) are equal, so both +DM and -DM are
       * zero.
       *
       * The rules remain the same when A=B and C=D (when the highs
       * equal the lows).
       *
       * When calculating the DM over a period > 1, the one-period DM
       * for the desired period are initialy sum. In other word,
       * for a -DM14, sum the -DM1 for the first 14 days (that's
       * 13 values because there is no DM for the first day!)
       * Subsequent DM are calculated using the Wilder's
       * smoothing approach:
       *
       *                                    Previous -DM14
       *  Today's -DM14 = Previous -DM14 -  -------------- + Today's -DM1
       *                                         14
       *
       * (Same thing for +DM14)
       *
       * Calculation of a -DI14 is as follow:
       *
       *               -DM14
       *     -DI14 =  --------
       *                TR14
       *
       * (Same thing for +DI14)
       *
       * Calculation of the TR14 is:
       *
       *                                   Previous TR14
       *    Today's TR14 = Previous TR14 - -------------- + Today's TR1
       *                                         14
       *
       *    The first TR14 is the summation of the first 14 TR1. See the
       *    TA_TRANGE function on how to calculate the true range.
       *
       * Calculation of the DX14 is:
       *
       *    diffDI = ABS( (-DI14) - (+DI14) )
       *    sumDI  = (-DI14) + (+DI14)
       *
       *    DX14 = 100 * (diffDI / sumDI)
       *
       * Calculation of the first ADX:
       *
       *    ADX14 = SUM of the first 14 DX
       *
       * Calculation of subsequent ADX:
       *
       *            ((Previous ADX14)*(14-1))+ Today's DX
       *    ADX14 = -------------------------------------
       *                             14
       *
       * Reference:
       *    New Concepts In Technical Trading Systems, J. Welles Wilder Jr
       */
      /* Original implementation from Wilder's book was doing some integer
       * rounding in its calculations.
       *
       * This was understandable in the context that at the time the book
       * was written, most user were doing the calculation by hand.
       *
       * For a computer, rounding is unnecessary (and even problematic when inputs
       * are close to 1).
       *
       * TA-Lib does not do the rounding. Still, if you want to reproduce Wilder's examples,
       * you can comment out the following #undef/#define and rebuild the library.
       */
      lookbackTotal = 2 * optInTimePeriod + TA_GLOBALS_UNSTABLE_PERIOD(TA_FUNC_UNST_ADX,Adx) - 1;
      /* Adjust startIdx to account for the lookback period. */
      if( startIdx < lookbackTotal )
      {
         startIdx = lookbackTotal;
      }
      /* Make sure there is still something to evaluate. */
      if( startIdx > endIdx )
      {
         dummyBegIdx = 0;
         dummyNBElement = 0;
         return TA_BAD_PARAM;
      }
      /* Indicate where the next output should be put
       * in the outReal.
       */
      outIdx = 0;
      /* Process the initial DM and TR */
      today = startIdx;
      dummyBegIdx = today;
      prevMinusDM = 0.0;
      prevPlusDM = 0.0;
      prevTR = 0.0;
      today = startIdx - lookbackTotal;
      prevHigh = inHigh[today];
      prevLow = inLow[today];
      prevClose = inClose[today];
      i = optInTimePeriod - 1;
      while( i-- > 0 )
      {
         /* Calculate the prevMinusDM and prevPlusDM */
         today += 1;
         tempReal = inHigh[today];
         diffP = tempReal - prevHigh;
         /* Plus Delta */
         prevHigh = tempReal;
         tempReal = inLow[today];
         diffM = prevLow - tempReal;
         /* Minus Delta */
         prevLow = tempReal;
         if( diffM > 0 && diffP < diffM )
         {
            /* Case 2 and 4: +DM=0,-DM=diffM */
            prevMinusDM += diffM;
         } else if( diffP > 0 && diffP > diffM )
         {
            /* Case 1 and 3: +DM=diffP,-DM=0 */
            prevPlusDM += diffP;
         }
         double _true_range_1;
         double range_1 = prevHigh - prevLow;
         double tmp_1 = fabs(prevHigh - prevClose);
         if( tmp_1 > range_1 )
         {
            range_1 = tmp_1;
         }
         tmp_1 = fabs(prevLow - prevClose);
         if( tmp_1 > range_1 )
         {
            range_1 = tmp_1;
         }
         _true_range_1 = range_1;
         tempReal = _true_range_1;
         prevTR += tempReal;
         prevClose = inClose[today];
      }
      /* Add up all the initial DX. */
      sumDX = 0.0;
      i = optInTimePeriod;
      while( i-- > 0 )
      {
         /* Calculate the prevMinusDM and prevPlusDM */
         today += 1;
         tempReal = inHigh[today];
         diffP = tempReal - prevHigh;
         /* Plus Delta */
         prevHigh = tempReal;
         tempReal = inLow[today];
         diffM = prevLow - tempReal;
         /* Minus Delta */
         prevLow = tempReal;
         prevMinusDM -= prevMinusDM / optInTimePeriod;
         prevPlusDM -= prevPlusDM / optInTimePeriod;
         if( diffM > 0 && diffP < diffM )
         {
            /* Case 2 and 4: +DM=0,-DM=diffM */
            prevMinusDM += diffM;
         } else if( diffP > 0 && diffP > diffM )
         {
            /* Case 1 and 3: +DM=diffP,-DM=0 */
            prevPlusDM += diffP;
         }
         /* Calculate the prevTR */
         double _true_range_2;
         double range_2 = prevHigh - prevLow;
         double tmp_2 = fabs(prevHigh - prevClose);
         if( tmp_2 > range_2 )
         {
            range_2 = tmp_2;
         }
         tmp_2 = fabs(prevLow - prevClose);
         if( tmp_2 > range_2 )
         {
            range_2 = tmp_2;
         }
         _true_range_2 = range_2;
         tempReal = _true_range_2;
         prevTR = prevTR - prevTR / optInTimePeriod + tempReal;
         prevClose = inClose[today];
         /* Calculate the DX. The value is rounded (see Wilder book). */
         if( !TA_IS_ZERO(prevTR) )
         {
            minusDI = (100.0 * (prevMinusDM / prevTR));
            plusDI = (100.0 * (prevPlusDM / prevTR));
            /* This loop is just to accumulate the initial DX */
            tempReal = minusDI + plusDI;
            if( !TA_IS_ZERO(tempReal) )
            {
               sumDX += (100.0 * (fabs(minusDI - plusDI) / tempReal));
            }
         }
      }
      /* Calculate the first ADX */
      prevADX = (sumDX / optInTimePeriod);
      /* Skip the unstable period */
      i = TA_GLOBALS_UNSTABLE_PERIOD(TA_FUNC_UNST_ADX,Adx);
      while( i-- > 0 )
      {
         /* Calculate the prevMinusDM and prevPlusDM */
         today += 1;
         tempReal = inHigh[today];
         diffP = tempReal - prevHigh;
         /* Plus Delta */
         prevHigh = tempReal;
         tempReal = inLow[today];
         diffM = prevLow - tempReal;
         /* Minus Delta */
         prevLow = tempReal;
         prevMinusDM -= prevMinusDM / optInTimePeriod;
         prevPlusDM -= prevPlusDM / optInTimePeriod;
         if( diffM > 0 && diffP < diffM )
         {
            /* Case 2 and 4: +DM=0,-DM=diffM */
            prevMinusDM += diffM;
         } else if( diffP > 0 && diffP > diffM )
         {
            /* Case 1 and 3: +DM=diffP,-DM=0 */
            prevPlusDM += diffP;
         }
         /* Calculate the prevTR */
         double _true_range_3;
         double range_3 = prevHigh - prevLow;
         double tmp_3 = fabs(prevHigh - prevClose);
         if( tmp_3 > range_3 )
         {
            range_3 = tmp_3;
         }
         tmp_3 = fabs(prevLow - prevClose);
         if( tmp_3 > range_3 )
         {
            range_3 = tmp_3;
         }
         _true_range_3 = range_3;
         tempReal = _true_range_3;
         prevTR = prevTR - prevTR / optInTimePeriod + tempReal;
         prevClose = inClose[today];
         if( !TA_IS_ZERO(prevTR) )
         {
            /* Calculate the DX. The value is rounded (see Wilder book). */
            minusDI = (100.0 * (prevMinusDM / prevTR));
            plusDI = (100.0 * (prevPlusDM / prevTR));
            tempReal = minusDI + plusDI;
            if( !TA_IS_ZERO(tempReal) )
            {
               tempReal = (100.0 * (fabs(minusDI - plusDI) / tempReal));
               /* Calculate the ADX */
               prevADX = ((prevADX * (optInTimePeriod - 1) + tempReal) / optInTimePeriod);
            }
         }
      }
      /* Output the first ADX */
      lastValue_outReal = prevADX;
      outIdx = 1;
      /* Calculate and output subsequent ADX */
      while( today < endIdx )
      {
         /* Calculate the prevMinusDM and prevPlusDM */
         today += 1;
         tempReal = inHigh[today];
         diffP = tempReal - prevHigh;
         /* Plus Delta */
         prevHigh = tempReal;
         tempReal = inLow[today];
         diffM = prevLow - tempReal;
         /* Minus Delta */
         prevLow = tempReal;
         prevMinusDM -= prevMinusDM / optInTimePeriod;
         prevPlusDM -= prevPlusDM / optInTimePeriod;
         if( diffM > 0 && diffP < diffM )
         {
            /* Case 2 and 4: +DM=0,-DM=diffM */
            prevMinusDM += diffM;
         } else if( diffP > 0 && diffP > diffM )
         {
            /* Case 1 and 3: +DM=diffP,-DM=0 */
            prevPlusDM += diffP;
         }
         /* Calculate the prevTR */
         double _true_range_4;
         double range_4 = prevHigh - prevLow;
         double tmp_4 = fabs(prevHigh - prevClose);
         if( tmp_4 > range_4 )
         {
            range_4 = tmp_4;
         }
         tmp_4 = fabs(prevLow - prevClose);
         if( tmp_4 > range_4 )
         {
            range_4 = tmp_4;
         }
         _true_range_4 = range_4;
         tempReal = _true_range_4;
         prevTR = prevTR - prevTR / optInTimePeriod + tempReal;
         prevClose = inClose[today];
         if( !TA_IS_ZERO(prevTR) )
         {
            /* Calculate the DX. The value is rounded (see Wilder book). */
            minusDI = (100.0 * (prevMinusDM / prevTR));
            plusDI = (100.0 * (prevPlusDM / prevTR));
            tempReal = minusDI + plusDI;
            if( !TA_IS_ZERO(tempReal) )
            {
               tempReal = (100.0 * (fabs(minusDI - plusDI) / tempReal));
               /* Calculate the ADX */
               prevADX = ((prevADX * (optInTimePeriod - 1) + tempReal) / optInTimePeriod);
            }
         }
         /* Output the ADX */
         lastValue_outReal = prevADX;
      }
      dummyNBElement = outIdx;

      /* Capture the live batch state into the handle. */
      sp = (struct TA_ADX_Stream *)TA_Malloc( sizeof(*sp) );
      if( !sp ) { return TA_ALLOC_ERR; }
      memset( sp, 0, sizeof(*sp) );
      sp->optInTimePeriod = optInTimePeriod;
      sp->prevHigh = prevHigh;
      sp->prevLow = prevLow;
      sp->prevClose = prevClose;
      sp->prevMinusDM = prevMinusDM;
      sp->prevPlusDM = prevPlusDM;
      sp->prevTR = prevTR;
      sp->tempReal = tempReal;
      sp->diffP = diffP;
      sp->diffM = diffM;
      sp->minusDI = minusDI;
      sp->plusDI = plusDI;
      sp->prevADX = prevADX;
      *outReal = lastValue_outReal;
      *stream = sp;
      return TA_SUCCESS;
   }
}

