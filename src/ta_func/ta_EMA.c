struct TA_EMA_Stream {
   int optInTimePeriod;
   double optInK_1;
   double prevMA;
};

/* Private function, not in public API. */
static void TA_EMA_StepInternal( struct TA_EMA_Stream *sp, double inReal, double *outReal )
{
   sp->prevMA = (inReal - sp->prevMA) * sp->optInK_1 + sp->prevMA;
   *outReal= sp->prevMA;
}

