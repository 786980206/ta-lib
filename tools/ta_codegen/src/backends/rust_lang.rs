fn gen_imports() -> String {
    "// Import types from parent module\n\
     use super::*;\n\n"
        .to_string()
}

fn gen_impl_block(func: &FuncDef) -> String {
    let mut out = String::new();
    let snake = func.name.to_lowercase();

    out.push_str(
        "// Allow non-snake-case names to maintain TA-Lib API compatibility\n\
         #[allow(non_snake_case)]\n\
         // allow unused variables and dead code due to gen code weirdness\n\
         #[allow(unused_variables)]\n\
         #[allow(dead_code)]\n\
         impl Core {\n",
    );

    out.push_str(&gen_lookback(func, &snake));
    out.push_str(&gen_func(func, &snake, false));
    out.push_str(&gen_func(func, &snake, true));

    out.push_str("}\n");
    out
}

fn gen_lookback(func: &FuncDef, snake: &str) -> String {
    let body = match &func.lookback {
        LookbackExpr::Literal(n) => format!("        return {};", n),
        LookbackExpr::ParamMinus(param, offset) => {
            format!("        return {} - {};", param, offset)
        }
    };

    // Build doc comment for lookback
    let mut out = String::new();
    out.push_str(&format!(
        "    /// Lookback period for [`Core::{}`].\n",
        snake
    ));
    out.push_str("    ///\n");
    out.push_str("    /// # Arguments\n");
    out.push_str("    ///\n");

    out.push_str(&format!(
        "    pub fn {}_lookback(&self) -> i32 {{\n{}\n    }}\n",
        snake, body
    ));
    out
}

fn gen_func(func: &FuncDef, snake: &str, single_precision: bool) -> String {
    let mut out = String::new();
    let suffix = if single_precision { "_s" } else { "" };
    let func_name = format!("{}{}", snake, suffix);

    // Doc comments
    if single_precision {
        out.push_str(&format!(
            "    /// Single-precision variant of [`Core::{}`].\n",
            snake
        ));
    } else {
        let title = func.description.as_deref().unwrap_or(&func.group);
        out.push_str(&format!("    /// {}\n", title));
        out.push_str("    ///\n");
        out.push_str("    /// # Arguments\n");
        out.push_str("    ///\n");
        out.push_str("    /// * `startIdx` - Start index for calculation range\n");
        out.push_str("    /// * `endIdx` - End index for calculation range (inclusive)\n");
        for input in &func.inputs {
            out.push_str(&format!(
                "    /// * `{}` - Input price series\n",
                input.name
            ));
        }
        out.push_str("    /// * `outBegIdx` - First valid output index\n");
        out.push_str("    /// * `outNBElement` - Number of valid output elements\n");
        for output in &func.outputs {
            out.push_str(&format!("    /// * `{}` - Output values\n", output.name));
        }
        out.push_str("    ///\n");
        out.push_str("    /// # Returns\n");
        out.push_str("    ///\n");
        out.push_str(
            "    /// [`RetCode::Success`] on success, or an error code on failure.\n",
        );
        out.push_str("    ///\n");
        out.push_str("    /// # Example\n");
        out.push_str("    ///\n");
        out.push_str("    /// ```\n");
        out.push_str("    /// use ta_lib::ta_func::{Core, RetCode};\n");
        out.push_str("    ///\n");
        // Generate example input declarations
        for _input in &func.inputs {
            out.push_str("    /// let input = [1.0_f64; 50];\n");
        }
        out.push_str("    /// let mut out = [0.0_f64; 50];\n");
        out.push_str("    /// let mut out_beg_idx: usize = 0;\n");
        out.push_str("    /// let mut out_nb_element: usize = 0;\n");
        out.push_str("    ///\n");
        out.push_str("    /// let core = Core::new();\n");
        out.push_str(&format!("    /// let result = core.{}(\n", snake));
        out.push_str("    ///  0,\n");
        out.push_str("    ///  49,\n");
        for _input in &func.inputs {
            out.push_str("    ///  &input,\n");
        }
        out.push_str("    ///  &mut out_beg_idx,\n");
        out.push_str("    ///  &mut out_nb_element,\n");
        out.push_str("    ///  &mut out,\n");
        out.push_str("    /// );\n");
        out.push_str("    ///\n");
        out.push_str("    /// assert_eq!(result, RetCode::Success);\n");
        out.push_str("    /// ```\n");
    }

    // Function signature
    let input_type = if single_precision { "f32" } else { "f64" };

    out.push_str(&format!("    pub fn {}(\n", func_name));
    out.push_str("        &self,\n");
    out.push_str("        startIdx: usize,\n");
    out.push_str("        endIdx: usize,\n");
    for input in &func.inputs {
        out.push_str(&format!(
            "        {}: &[{}],\n",
            input.name, input_type
        ));
    }
    for opt in &func.optional_inputs {
        let rust_type = match opt.param_type {
            ParamType::Real => "f64",
            ParamType::Integer => "i32",
        };
        out.push_str(&format!("        {}: {},\n", opt.name, rust_type));
    }
    out.push_str("        outBegIdx: &mut usize,\n");
    out.push_str("        outNBElement: &mut usize,\n");
    for output in &func.outputs {
        let rust_type = match output.param_type {
            ParamType::Real => "f64",
            ParamType::Integer => "i32",
        };
        out.push_str(&format!(
            "        {}: &mut [{}],\n",
            output.name, rust_type
        ));
    }
    out.push_str("    ) -> RetCode {\n");

    // Declare local variables (excluding loop iterators consumed by for-loops)
    let for_loop_vars = collect_for_loop_vars(&func.body);
    // Build map of VarDecl init expressions for for-loop start values
    let var_inits: std::collections::HashMap<String, &Expr> = func
        .body
        .iter()
        .filter_map(|s| {
            if let Statement::VarDecl { name, init, .. } = s {
                Some((name.clone(), init))
            } else {
                None
            }
        })
        .collect();
    for stmt in &func.body {
        if let Statement::VarDecl {
            var_type, name, ..
        } = stmt
        {
            if for_loop_vars.contains(name) {
                continue;
            }
            let rust_type = match var_type {
                VarType::Real => "f64",
                VarType::Integer => "i32",
                VarType::Index => "usize",
            };
            out.push_str(&format!("        let mut {}: {};\n", name, rust_type));
        }
    }

    // Validation (no startIdx < 0 for usize)
    out.push_str("        if endIdx < startIdx {\n");
    out.push_str("            return RetCode::OutOfRangeEndIndex;\n");
    out.push_str("        }\n");

    // Emit VarDecl initializations (excluding for-loop vars)
    for stmt in &func.body {
        if let Statement::VarDecl { name, init, .. } = stmt {
            if for_loop_vars.contains(name) {
                continue;
            }
            out.push_str(&format!(
                "        {} = {};\n",
                name,
                render_expr(init, single_precision)
            ));
        }
    }

    // Collect output array names for cast insertion
    let output_names: Vec<String> = func.outputs.iter().map(|o| o.name.clone()).collect();

    // Render body statements
    for stmt in &func.body {
        if matches!(stmt, Statement::VarDecl { .. }) {
            continue;
        }
        out.push_str(&render_statement(stmt, 8, single_precision, &for_loop_vars, &var_inits, &output_names));
    }

    out.push_str("        return RetCode::Success;\n");
    out.push_str("    }\n");

    out
}

/// Detect while loops that are simple for-loop patterns:
/// `while i <= endIdx { ... i += 1; }` where i is declared as `index i = startIdx`
/// Returns the set of variable names used as loop iterators.
fn collect_for_loop_vars(body: &[Statement]) -> Vec<String> {
    let mut vars = Vec::new();
    // Find VarDecl + While pairs
    let decls: std::collections::HashMap<String, &Expr> = body
        .iter()
        .filter_map(|s| {
            if let Statement::VarDecl { name, init, var_type } = s {
                if *var_type == VarType::Index {
                    return Some((name.clone(), init));
                }
            }
            None
        })
        .collect();

    for stmt in body {
        if let Statement::While { condition, body: while_body } = stmt {
            if let Some(iter_var) = detect_for_pattern(condition, while_body, &decls) {
                vars.push(iter_var);
            }
        }
    }
    vars
}

/// Check if a while loop matches the pattern: while iter_var <= bound { ... iter_var += 1 }
fn detect_for_pattern(
    condition: &Expr,
    while_body: &[Statement],
    decls: &std::collections::HashMap<String, &Expr>,
) -> Option<String> {
    // condition must be BinOp(Var(iter), LessEq, Var(bound))
    if let Expr::BinOp(left, BinOp::LessEq, _right) = condition {
        if let Expr::Var(iter_name) = left.as_ref() {
            // Check that iter_name is declared as an index var
            if decls.contains_key(iter_name) {
                // Check last statement is iter_name += 1
                if let Some(Statement::Assign { target, value }) = while_body.last() {
                    if let Expr::Var(tname) = target {
                        if tname == iter_name {
                            if let Expr::BinOp(l, BinOp::Add, r) = value {
                                if let (Expr::Var(ln), Expr::IntLiteral(1)) =
                                    (l.as_ref(), r.as_ref())
                                {
                                    if ln == iter_name {
                                        return Some(iter_name.clone());
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    None
}

fn render_statement(
    stmt: &Statement,
    indent: usize,
    single_precision: bool,
    for_loop_vars: &[String],
    var_inits: &std::collections::HashMap<String, &Expr>,
    output_names: &[String],
) -> String {
    let pad = " ".repeat(indent);
    match stmt {
        Statement::VarDecl { .. } => String::new(), // already handled
        Statement::Assign { target, value } => {
            // Detect compound assignment: x = x + expr => x += expr
            if let (Expr::Var(tname), Expr::BinOp(left, op, right)) = (target, value) {
                if let Expr::Var(lname) = left.as_ref() {
                    if lname == tname {
                        let op_str = match op {
                            BinOp::Add => "+=",
                            BinOp::Sub => "-=",
                            BinOp::Mul => "*=",
                            BinOp::Div => "/=",
                            _ => "",
                        };
                        if !op_str.is_empty() {
                            let target_str =
                                render_assign_target(target, single_precision);
                            return format!(
                                "{}{} {} {};\n",
                                pad,
                                target_str,
                                op_str,
                                render_expr(right, single_precision)
                            );
                        }
                    }
                }
            }
            let target_str = render_assign_target(target, single_precision);
            let value_str = render_expr(value, single_precision);
            // Add `as f64` cast for output array assignments
            let needs_cast = if let Expr::ArrayAccess(name, _) = target {
                output_names.contains(name)
            } else {
                false
            };
            if needs_cast {
                format!("{}{} = ({}) as f64;\n", pad, target_str, value_str)
            } else {
                format!("{}{} = {};\n", pad, target_str, value_str)
            }
        }
        Statement::While {
            condition,
            body: while_body,
        } => {
            // Check if this is a for-loop pattern
            if let Expr::BinOp(left, BinOp::LessEq, right) = condition {
                if let Expr::Var(iter_name) = left.as_ref() {
                    if for_loop_vars.contains(iter_name) {
                        let start_expr = if let Some(init) = var_inits.get(iter_name) {
                            render_expr(init, single_precision)
                        } else {
                            iter_name.clone()
                        };
                        let end_expr = render_expr(right, single_precision);
                        let mut out = format!(
                            "{}for {} in ({} as usize)..=({} as usize) {{\n",
                            pad, iter_name, start_expr, end_expr
                        );
                        // Render body except last statement (the i += 1)
                        for s in &while_body[..while_body.len() - 1] {
                            out.push_str(&render_statement(
                                s,
                                indent + 4,
                                single_precision,
                                for_loop_vars,
                                var_inits,
                                output_names,
                            ));
                        }
                        out.push_str(&format!("{}}}\n", pad));
                        return out;
                    }
                }
            }
            // Regular while loop
            let mut out = format!(
                "{}while {} {{\n",
                pad,
                render_expr(condition, single_precision)
            );
            for s in while_body {
                out.push_str(&render_statement(s, indent + 4, single_precision, for_loop_vars, var_inits, output_names));
            }
            out.push_str(&format!("{}}}\n", pad));
            out
        }
    }
}

fn render_assign_target(expr: &Expr, single_precision: bool) -> String {
    match expr {
        Expr::Var(name) if name == "outBegIdx" || name == "outNBElement" => {
            format!("(*{})", name)
        }
        Expr::Var(name) => name.clone(),
        Expr::ArrayAccess(name, idx) => {
            format!("{}[{}]", name, render_expr(idx, single_precision))
        }
        _ => render_expr(expr, single_precision),
    }
}

fn render_expr(expr: &Expr, single_precision: bool) -> String {
    match expr {
        Expr::Literal(f) => {
            if *f == f.floor() && f.abs() < 1e15 {
                format!("{}", *f as i64)
            } else {
                format!("{}", f)
            }
        }
        Expr::IntLiteral(i) => format!("{}", i),
        Expr::Var(name) => name.clone(),
        Expr::ArrayAccess(name, idx) => {
            format!("{}[{}]", name, render_expr(idx, single_precision))
        }
        Expr::BinOp(left, op, right) => {
            let op_str = match op {
                BinOp::Add => " + ",
                BinOp::Sub => " - ",
                BinOp::Mul => " * ",
                BinOp::Div => " / ",
                BinOp::LessEq => " <= ",
                BinOp::Less => " < ",
                BinOp::Greater => " > ",
                BinOp::GreaterEq => " >= ",
                BinOp::Eq => " == ",
            };
            format!(
                "{}{}{}",
                render_expr(left, single_precision),
                op_str,
                render_expr(right, single_precision)
            )
        }
        Expr::Cast(var_type, inner) => {
            let rust_type = match var_type {
                VarType::Real => "f64",
                VarType::Integer => "i32",
                VarType::Index => "usize",
            };
            format!("({}) as {}", render_expr(inner, single_precision), rust_type)
        }
    }
}

fn gen_footer() -> String {
    "/* Generated */\n\n\
     /***************/\n\
     /* End of File */\n\
     /***************/\n"
        .to_string()
}
