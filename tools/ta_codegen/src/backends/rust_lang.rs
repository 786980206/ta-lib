pub fn generate(func: &FuncDef) -> String {
    let mut out = String::new();
    out.push_str(&gen_header());
    out.push_str(&gen_imports());
    out.push_str(&gen_impl_block(func));
    out.push_str(&gen_footer());
    out
}

