#!/usr/bin/env python3
"""Generate bench_data.h with 100k deterministic price points."""

import struct
from pathlib import Path

BENCH_LEN = 100_000
SCRIPT_DIR = Path(__file__).parent


def lcg_prices(length: int, seed: int) -> list[float]:
    """Deterministic random-walk prices using LCG PRNG."""
    state = seed
    prices = [100.0]
    A = 6364136223846793005
    B = 1442695040888963407
    MOD = 1 << 64
    for _ in range(1, length):
        state = (state * A + B) % MOD
        step = ((state >> 33) / (1 << 31)) - 0.5
        prices.append(prices[-1] + step)
    return prices


def format_array(name: str, values: list[float], per_line: int = 8) -> list[str]:
    """Format a C static const double array."""
    lines = [f"static const double {name}[] = {{"]
    for i in range(0, len(values), per_line):
        chunk = values[i : i + per_line]
        formatted = ", ".join(f"{v:.17g}" for v in chunk)
        lines.append(f"    {formatted},")
    lines.append("};")
    lines.append(
        f"static const int {name}_LEN = sizeof({name}) / sizeof(double);"
    )
    return lines


def main():
    prices = lcg_prices(BENCH_LEN, seed=42)
    prices_2 = lcg_prices(BENCH_LEN, seed=137)

    header = [
        "#ifndef BENCH_DATA_H",
        "#define BENCH_DATA_H",
        "",
        f"/* {BENCH_LEN} deterministic price points for benchmarking.",
        " * Generated by generate_bench_data.py — do not edit. */",
        "",
    ]
    header += format_array("BENCH_PRICES", prices)
    header += [""]
    header += format_array("BENCH_PRICES_2", prices_2)
    header += ["", "#endif /* BENCH_DATA_H */", ""]

    out_path = SCRIPT_DIR / "bench_data.h"
    out_path.write_text("\n".join(header))
    print(f"Generated {out_path} ({BENCH_LEN} points per array)")


if __name__ == "__main__":
    main()
