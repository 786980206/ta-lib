        let mut prevLow: f64 = 0.0_f64;
        let mut af: f64 = 0.0_f64;
        let mut ep: f64 = 0.0_f64;
        let mut sar: f64 = 0.0_f64;
        let mut ep_temp: [f64; 1 as usize] = [0.0_f64; 1 as usize];
