impl Core {
    fn cdlbelthold_step_internal(&self, sp: &mut CdlbeltholdStreamState, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64, outInteger: &mut i32) {
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let ShadowVeryShort_rangeType: i32 = self.candle_settings.shadow_very_short.range_type;
        #[allow(non_snake_case)]
        let ShadowVeryShort_avgPeriod: i32 = self.candle_settings.shadow_very_short.avg_period;
        #[allow(non_snake_case)]
        let ShadowVeryShort_factor: f64 = self.candle_settings.shadow_very_short.factor;
        if sp.ringCap_BodyLongTrailingIdx == 0 {
            sp.ring_BodyLongTrailingIdx_inOpen[0] = inOpen;
            sp.ring_BodyLongTrailingIdx_inHigh[0] = inHigh;
            sp.ring_BodyLongTrailingIdx_inLow[0] = inLow;
            sp.ring_BodyLongTrailingIdx_inClose[0] = inClose;
        }
        if sp.ringCap_ShadowVeryShortTrailingIdx == 0 {
            sp.ring_ShadowVeryShortTrailingIdx_inOpen[0] = inOpen;
            sp.ring_ShadowVeryShortTrailingIdx_inHigh[0] = inHigh;
            sp.ring_ShadowVeryShortTrailingIdx_inLow[0] = inLow;
            sp.ring_ShadowVeryShortTrailingIdx_inClose[0] = inClose;
        }
        if (inClose - inOpen).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (sp.BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (inClose - inOpen).abs(), 1 => (inHigh) - (inLow), _ => (inHigh) - (inLow) - ((inClose) - (inOpen)).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // long body
           ((if inClose >= inOpen { 1 } else { 0 - 1 }) == 1 && ((if inClose >= inOpen { inOpen } else { inClose }) - inLow) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (sp.ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose - inOpen).abs(), 1 => (inHigh) - (inLow), _ => (inHigh) - (inLow) - ((inClose) - (inOpen)).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 })) || ((if inClose >= inOpen { 1 } else { 0 - 1 })) as i32 == 0 - 1 && (inHigh - (if inClose >= inOpen { inClose } else { inOpen })) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (sp.ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose - inOpen).abs(), 1 => (inHigh) - (inLow), _ => (inHigh) - (inLow) - ((inClose) - (inOpen)).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 }))) // white body and very short lower shadow black body and very short upper shadow
        {
            (*outInteger) = ((if inClose >= inOpen { 1 } else { 0 - 1 }) * 100) as i32;
        } else {
            (*outInteger) = 0;
        }
        // add the current range and subtract the first range: this is done after the pattern recognition
        // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
        let mut _candlerange_0: f64;
        match BodyLong_rangeType {
            0 => {
                _candlerange_0 = (inClose - inOpen).abs();
            }
            1 => {
                _candlerange_0 = inHigh - inLow;
            }
            2 => {
                _candlerange_0 = inHigh - inLow - (inClose - inOpen).abs();
            }
            _ => {
                _candlerange_0 = 0.0;
            }
        }
        let mut _candlerange_1: f64;
        match BodyLong_rangeType {
            0 => {
                _candlerange_1 = (sp.ring_BodyLongTrailingIdx_inClose[sp.ringPos_BodyLongTrailingIdx] - sp.ring_BodyLongTrailingIdx_inOpen[sp.ringPos_BodyLongTrailingIdx]).abs();
            }
            1 => {
                _candlerange_1 = sp.ring_BodyLongTrailingIdx_inHigh[sp.ringPos_BodyLongTrailingIdx] - sp.ring_BodyLongTrailingIdx_inLow[sp.ringPos_BodyLongTrailingIdx];
            }
            2 => {
                _candlerange_1 = sp.ring_BodyLongTrailingIdx_inHigh[sp.ringPos_BodyLongTrailingIdx] - sp.ring_BodyLongTrailingIdx_inLow[sp.ringPos_BodyLongTrailingIdx] - (sp.ring_BodyLongTrailingIdx_inClose[sp.ringPos_BodyLongTrailingIdx] - sp.ring_BodyLongTrailingIdx_inOpen[sp.ringPos_BodyLongTrailingIdx]).abs();
            }
            _ => {
                _candlerange_1 = 0.0;
            }
        }
        sp.BodyLongPeriodTotal += _candlerange_0 - _candlerange_1;
        let mut _candlerange_2: f64;
        match ShadowVeryShort_rangeType {
            0 => {
                _candlerange_2 = (inClose - inOpen).abs();
            }
            1 => {
                _candlerange_2 = inHigh - inLow;
            }
            2 => {
                _candlerange_2 = inHigh - inLow - (inClose - inOpen).abs();
            }
            _ => {
                _candlerange_2 = 0.0;
            }
        }
        let mut _candlerange_3: f64;
        match ShadowVeryShort_rangeType {
            0 => {
                _candlerange_3 = (sp.ring_ShadowVeryShortTrailingIdx_inClose[sp.ringPos_ShadowVeryShortTrailingIdx] - sp.ring_ShadowVeryShortTrailingIdx_inOpen[sp.ringPos_ShadowVeryShortTrailingIdx]).abs();
            }
            1 => {
                _candlerange_3 = sp.ring_ShadowVeryShortTrailingIdx_inHigh[sp.ringPos_ShadowVeryShortTrailingIdx] - sp.ring_ShadowVeryShortTrailingIdx_inLow[sp.ringPos_ShadowVeryShortTrailingIdx];
            }
            2 => {
                _candlerange_3 = sp.ring_ShadowVeryShortTrailingIdx_inHigh[sp.ringPos_ShadowVeryShortTrailingIdx] - sp.ring_ShadowVeryShortTrailingIdx_inLow[sp.ringPos_ShadowVeryShortTrailingIdx] - (sp.ring_ShadowVeryShortTrailingIdx_inClose[sp.ringPos_ShadowVeryShortTrailingIdx] - sp.ring_ShadowVeryShortTrailingIdx_inOpen[sp.ringPos_ShadowVeryShortTrailingIdx]).abs();
            }
            _ => {
                _candlerange_3 = 0.0;
            }
        }
        sp.ShadowVeryShortPeriodTotal += _candlerange_2 - _candlerange_3;
        sp.ring_BodyLongTrailingIdx_inOpen[sp.ringPos_BodyLongTrailingIdx] = inOpen;
        sp.ring_BodyLongTrailingIdx_inHigh[sp.ringPos_BodyLongTrailingIdx] = inHigh;
        sp.ring_BodyLongTrailingIdx_inLow[sp.ringPos_BodyLongTrailingIdx] = inLow;
        sp.ring_BodyLongTrailingIdx_inClose[sp.ringPos_BodyLongTrailingIdx] = inClose;
        sp.ringPos_BodyLongTrailingIdx = sp.ringPos_BodyLongTrailingIdx + 1;
        if sp.ringPos_BodyLongTrailingIdx >= sp.ringCap_BodyLongTrailingIdx {
            sp.ringPos_BodyLongTrailingIdx = 0;
        }
        sp.ring_ShadowVeryShortTrailingIdx_inOpen[sp.ringPos_ShadowVeryShortTrailingIdx] = inOpen;
        sp.ring_ShadowVeryShortTrailingIdx_inHigh[sp.ringPos_ShadowVeryShortTrailingIdx] = inHigh;
        sp.ring_ShadowVeryShortTrailingIdx_inLow[sp.ringPos_ShadowVeryShortTrailingIdx] = inLow;
        sp.ring_ShadowVeryShortTrailingIdx_inClose[sp.ringPos_ShadowVeryShortTrailingIdx] = inClose;
        sp.ringPos_ShadowVeryShortTrailingIdx = sp.ringPos_ShadowVeryShortTrailingIdx + 1;
        if sp.ringPos_ShadowVeryShortTrailingIdx >= sp.ringCap_ShadowVeryShortTrailingIdx {
            sp.ringPos_ShadowVeryShortTrailingIdx = 0;
        }
    }

    /// Internal startIdx-anchored open behind [`Core::cdlbelthold_open`] (composition seam).
    pub(crate) fn cdlbelthold_open_internal(
        &self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], startIdx: usize,
    ) -> Result<(CdlbeltholdStream, i32), RetCode> {
        if inOpen.is_empty() || inHigh.is_empty() || inLow.is_empty() || inClose.is_empty() || inHigh.len() != inOpen.len() || inLow.len() != inOpen.len() || inClose.len() != inOpen.len() {
            return Err(RetCode::BadParam);
        }
        if inOpen.len() > i32::MAX as usize {
            return Err(RetCode::BadParam);
        }
        let historyLen: usize = inOpen.len();
        let endIdx: usize = historyLen - 1;
        let mut startIdx = startIdx;
        let mut dummyBegIdx: usize = 0;
        let mut dummyNBElement: usize = 0;
        let mut lastValue_outInteger: i32 = 0_i32;
        let mut BodyLongPeriodTotal: f64 = 0.0_f64;
        let mut ShadowVeryShortPeriodTotal: f64 = 0.0_f64;
        let mut i: usize = 0_usize;
        let mut outIdx: usize = 0_usize;
        let mut BodyLongTrailingIdx: usize = 0_usize;
        let mut ShadowVeryShortTrailingIdx: usize = 0_usize;
        let mut lookbackTotal: usize = 0_usize;
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let ShadowVeryShort_rangeType: i32 = self.candle_settings.shadow_very_short.range_type;
        #[allow(non_snake_case)]
        let ShadowVeryShort_avgPeriod: i32 = self.candle_settings.shadow_very_short.avg_period;
        #[allow(non_snake_case)]
        let ShadowVeryShort_factor: f64 = self.candle_settings.shadow_very_short.factor;
        // Identify the minimum number of price bar needed
        // to calculate at least one output.
        lookbackTotal = self.cdlbelthold_lookback();
        // Move up the start index if there is not
        // enough initial data.
        if startIdx < lookbackTotal {
            startIdx = lookbackTotal;
        }
        // Make sure there is still something to evaluate.
        if startIdx > endIdx {
            dummyBegIdx = 0;
            dummyNBElement = 0;
            return Err(RetCode::BadParam);
        }
        // Do the calculation using tight loops.
        // Add-up the initial period, except for the last value.
        BodyLongPeriodTotal = 0.0;
        BodyLongTrailingIdx = startIdx - (BodyLong_avgPeriod) as usize;
        ShadowVeryShortPeriodTotal = 0.0;
        ShadowVeryShortTrailingIdx = startIdx - (ShadowVeryShort_avgPeriod) as usize;
        i = BodyLongTrailingIdx;
        while i < startIdx {
            let mut _candlerange_4: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_4 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_4 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_4 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_4 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_4;
            i += 1;
        }
        i = ShadowVeryShortTrailingIdx;
        while i < startIdx {
            let mut _candlerange_5: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_5 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_5 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_5 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_5 = 0.0;
                }
            }
            ShadowVeryShortPeriodTotal += _candlerange_5;
            i += 1;
        }
        // Proceed with the calculation for the requested range.
        // Must have:
        // - long white (black) real body
        // - no or very short lower (upper) shadow
        // The meaning of "long" and "very short" is specified with TA_SetCandleSettings
        // outInteger is positive (1 to 100) when white (bullish), negative (-1 to -100) when black (bearish)
        outIdx = 0;
        loop {
            if (inClose[i] - inOpen[i]).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // long body
               ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 }) == 1 && ((if inClose[i] >= inOpen[i] { inOpen[i] } else { inClose[i] }) - inLow[i]) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 })) || ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && (inHigh[i] - (if inClose[i] >= inOpen[i] { inClose[i] } else { inOpen[i] })) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 }))) // white body and very short lower shadow black body and very short upper shadow
            {
                lastValue_outInteger = ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 }) * 100) as i32;
            } else {
                lastValue_outInteger = 0;
            }
            // add the current range and subtract the first range: this is done after the pattern recognition
            // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
            let mut _candlerange_6: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_6 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_6 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_6 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_6 = 0.0;
                }
            }
            let mut _candlerange_7: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_7 = (inClose[BodyLongTrailingIdx] - inOpen[BodyLongTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_7 = inHigh[BodyLongTrailingIdx] - inLow[BodyLongTrailingIdx];
                }
                2 => {
                    _candlerange_7 = inHigh[BodyLongTrailingIdx] - inLow[BodyLongTrailingIdx] - (inClose[BodyLongTrailingIdx] - inOpen[BodyLongTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_7 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_6 - _candlerange_7;
            let mut _candlerange_8: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_8 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_8 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_8 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_8 = 0.0;
                }
            }
            let mut _candlerange_9: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_9 = (inClose[ShadowVeryShortTrailingIdx] - inOpen[ShadowVeryShortTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_9 = inHigh[ShadowVeryShortTrailingIdx] - inLow[ShadowVeryShortTrailingIdx];
                }
                2 => {
                    _candlerange_9 = inHigh[ShadowVeryShortTrailingIdx] - inLow[ShadowVeryShortTrailingIdx] - (inClose[ShadowVeryShortTrailingIdx] - inOpen[ShadowVeryShortTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_9 = 0.0;
                }
            }
            ShadowVeryShortPeriodTotal += _candlerange_8 - _candlerange_9;
            i += 1;
            BodyLongTrailingIdx += 1;
            ShadowVeryShortTrailingIdx += 1;
            if !(i <= endIdx) { break; }
        }
        // All done. Indicate the output limits and return.
        dummyNBElement = outIdx;
        dummyBegIdx = startIdx;

        // Capture the live batch state into the handle.
        let cap_BodyLongTrailingIdx: i64 = (i as i64) - (BodyLongTrailingIdx as i64);
        if cap_BodyLongTrailingIdx < 0 || cap_BodyLongTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyLongTrailingIdx: usize = if cap_BodyLongTrailingIdx > 0 { cap_BodyLongTrailingIdx as usize } else { 1 };
        let mut ring_BodyLongTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inOpen[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inHigh[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inLow[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inClose[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let cap_ShadowVeryShortTrailingIdx: i64 = (i as i64) - (ShadowVeryShortTrailingIdx as i64);
        if cap_ShadowVeryShortTrailingIdx < 0 || cap_ShadowVeryShortTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_ShadowVeryShortTrailingIdx: usize = if cap_ShadowVeryShortTrailingIdx > 0 { cap_ShadowVeryShortTrailingIdx as usize } else { 1 };
        let mut ring_ShadowVeryShortTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inOpen[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inHigh[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inLow[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inClose[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let state = CdlbeltholdStreamState {
            BodyLongPeriodTotal,
            ShadowVeryShortPeriodTotal,
            ringPos_BodyLongTrailingIdx: 0_usize,
            ringCap_BodyLongTrailingIdx: cap_BodyLongTrailingIdx as usize,
            ring_BodyLongTrailingIdx_inOpen,
            ring_BodyLongTrailingIdx_inHigh,
            ring_BodyLongTrailingIdx_inLow,
            ring_BodyLongTrailingIdx_inClose,
            ringPos_ShadowVeryShortTrailingIdx: 0_usize,
            ringCap_ShadowVeryShortTrailingIdx: cap_ShadowVeryShortTrailingIdx as usize,
            ring_ShadowVeryShortTrailingIdx_inOpen,
            ring_ShadowVeryShortTrailingIdx_inHigh,
            ring_ShadowVeryShortTrailingIdx_inLow,
            ring_ShadowVeryShortTrailingIdx_inClose,
        };
        Ok((CdlbeltholdStream { core: self.clone(), state }, lastValue_outInteger))
    }

    /// Open a live CDLBELTHOLD stream over the warm-up history; returns the handle and
    /// the value at the last history bar — bit-identical to [`Core::cdlbelthold`] at that bar.
    ///
    /// # Errors
    ///
    /// [`RetCode::BadParam`] when a parameter is out of range, an input is empty or
    /// input lengths differ, or the history is shorter than `lookback + 1` bars.
    ///
    /// ```
    /// use ta_lib::Core;
    /// let open: Vec<f64> = (0..252).map(|i| 100.0 + 10.0 * (0.1 * i as f64 - 0.05).sin()).collect();
    /// let high: Vec<f64> = (0..252).map(|i| 101.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    /// let low: Vec<f64> = (0..252).map(|i| 99.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    /// let close: Vec<f64> = (0..252).map(|i| 100.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    ///
    /// let core = Core::new();
    /// let (mut s, _last) = core.cdlbelthold_open(&open, &high, &low, &close).expect("enough history");
    /// let peeked = s.peek(100.2, 101.4, 99.1, 100.9);
    /// let updated = s.update(100.2, 101.4, 99.1, 100.9);
    /// assert_eq!(peeked, updated);
    /// ```
    #[doc(alias = "TA_CDLBELTHOLD_Open")]
    pub fn cdlbelthold_open(&self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], ) -> Result<(CdlbeltholdStream, i32), RetCode> {
        self.cdlbelthold_open_internal(inOpen, inHigh, inLow, inClose, 0)
    }

    /// [`Core::cdlbelthold_open`] that also fills the output array(s) bit-identically to
    /// [`Core::cdlbelthold`] over `0..len` in the same single pass. Output slices must hold
    /// `len - lookback` values; undersized slices panic (the batch sizing contract).
    #[doc(alias = "TA_CDLBELTHOLD_OpenAndFill")]
    pub fn cdlbelthold_open_and_fill(
        &self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], outBegIdx: &mut usize, outNBElement: &mut usize, outInteger: &mut [i32],
    ) -> Result<CdlbeltholdStream, RetCode> {
        if inOpen.is_empty() || inHigh.is_empty() || inLow.is_empty() || inClose.is_empty() || inHigh.len() != inOpen.len() || inLow.len() != inOpen.len() || inClose.len() != inOpen.len() {
            return Err(RetCode::BadParam);
        }
        if inOpen.len() > i32::MAX as usize {
            return Err(RetCode::BadParam);
        }
        let historyLen: usize = inOpen.len();
        let endIdx: usize = historyLen - 1;
        let mut startIdx: usize = 0;
        let mut dummyBegIdx: usize = 0;
        let mut dummyNBElement: usize = 0;
        let mut BodyLongPeriodTotal: f64 = 0.0_f64;
        let mut ShadowVeryShortPeriodTotal: f64 = 0.0_f64;
        let mut i: usize = 0_usize;
        let mut outIdx: usize = 0_usize;
        let mut BodyLongTrailingIdx: usize = 0_usize;
        let mut ShadowVeryShortTrailingIdx: usize = 0_usize;
        let mut lookbackTotal: usize = 0_usize;
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let ShadowVeryShort_rangeType: i32 = self.candle_settings.shadow_very_short.range_type;
        #[allow(non_snake_case)]
        let ShadowVeryShort_avgPeriod: i32 = self.candle_settings.shadow_very_short.avg_period;
        #[allow(non_snake_case)]
        let ShadowVeryShort_factor: f64 = self.candle_settings.shadow_very_short.factor;
        // Identify the minimum number of price bar needed
        // to calculate at least one output.
        lookbackTotal = self.cdlbelthold_lookback();
        // Move up the start index if there is not
        // enough initial data.
        if startIdx < lookbackTotal {
            startIdx = lookbackTotal;
        }
        // Make sure there is still something to evaluate.
        if startIdx > endIdx {
            (*outBegIdx) = 0;
            (*outNBElement) = 0;
            return Err(RetCode::BadParam);
        }
        // Do the calculation using tight loops.
        // Add-up the initial period, except for the last value.
        BodyLongPeriodTotal = 0.0;
        BodyLongTrailingIdx = startIdx - (BodyLong_avgPeriod) as usize;
        ShadowVeryShortPeriodTotal = 0.0;
        ShadowVeryShortTrailingIdx = startIdx - (ShadowVeryShort_avgPeriod) as usize;
        i = BodyLongTrailingIdx;
        while i < startIdx {
            let mut _candlerange_10: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_10 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_10 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_10 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_10 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_10;
            i += 1;
        }
        i = ShadowVeryShortTrailingIdx;
        while i < startIdx {
            let mut _candlerange_11: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_11 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_11 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_11 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_11 = 0.0;
                }
            }
            ShadowVeryShortPeriodTotal += _candlerange_11;
            i += 1;
        }
        // Proceed with the calculation for the requested range.
        // Must have:
        // - long white (black) real body
        // - no or very short lower (upper) shadow
        // The meaning of "long" and "very short" is specified with TA_SetCandleSettings
        // outInteger is positive (1 to 100) when white (bullish), negative (-1 to -100) when black (bearish)
        outIdx = 0;
        loop {
            if (inClose[i] - inOpen[i]).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // long body
               ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 }) == 1 && ((if inClose[i] >= inOpen[i] { inOpen[i] } else { inClose[i] }) - inLow[i]) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 })) || ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && (inHigh[i] - (if inClose[i] >= inOpen[i] { inClose[i] } else { inOpen[i] })) < ((ShadowVeryShort_factor) * (if (ShadowVeryShort_avgPeriod) != 0 { (ShadowVeryShortPeriodTotal) / (ShadowVeryShort_avgPeriod as f64) } else { match ShadowVeryShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (ShadowVeryShort_rangeType) == 2 { 2.0 } else { 1.0 }))) // white body and very short lower shadow black body and very short upper shadow
            {
                outInteger[outIdx] = ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 }) * 100) as i32;
                outIdx += 1;
            } else {
                outInteger[outIdx] = 0;
                outIdx += 1;
            }
            // add the current range and subtract the first range: this is done after the pattern recognition
            // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
            let mut _candlerange_12: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_12 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_12 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_12 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_12 = 0.0;
                }
            }
            let mut _candlerange_13: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_13 = (inClose[BodyLongTrailingIdx] - inOpen[BodyLongTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_13 = inHigh[BodyLongTrailingIdx] - inLow[BodyLongTrailingIdx];
                }
                2 => {
                    _candlerange_13 = inHigh[BodyLongTrailingIdx] - inLow[BodyLongTrailingIdx] - (inClose[BodyLongTrailingIdx] - inOpen[BodyLongTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_13 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_12 - _candlerange_13;
            let mut _candlerange_14: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_14 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_14 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_14 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_14 = 0.0;
                }
            }
            let mut _candlerange_15: f64;
            match ShadowVeryShort_rangeType {
                0 => {
                    _candlerange_15 = (inClose[ShadowVeryShortTrailingIdx] - inOpen[ShadowVeryShortTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_15 = inHigh[ShadowVeryShortTrailingIdx] - inLow[ShadowVeryShortTrailingIdx];
                }
                2 => {
                    _candlerange_15 = inHigh[ShadowVeryShortTrailingIdx] - inLow[ShadowVeryShortTrailingIdx] - (inClose[ShadowVeryShortTrailingIdx] - inOpen[ShadowVeryShortTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_15 = 0.0;
                }
            }
            ShadowVeryShortPeriodTotal += _candlerange_14 - _candlerange_15;
            i += 1;
            BodyLongTrailingIdx += 1;
            ShadowVeryShortTrailingIdx += 1;
            if !(i <= endIdx) { break; }
        }
        // All done. Indicate the output limits and return.
        (*outNBElement) = outIdx;
        (*outBegIdx) = startIdx;

        // Capture the live batch state into the handle.
        let cap_BodyLongTrailingIdx: i64 = (i as i64) - (BodyLongTrailingIdx as i64);
        if cap_BodyLongTrailingIdx < 0 || cap_BodyLongTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyLongTrailingIdx: usize = if cap_BodyLongTrailingIdx > 0 { cap_BodyLongTrailingIdx as usize } else { 1 };
        let mut ring_BodyLongTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inOpen[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inHigh[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inLow[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let mut ring_BodyLongTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        ring_BodyLongTrailingIdx_inClose[..cap_BodyLongTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_BodyLongTrailingIdx as usize..]);
        let cap_ShadowVeryShortTrailingIdx: i64 = (i as i64) - (ShadowVeryShortTrailingIdx as i64);
        if cap_ShadowVeryShortTrailingIdx < 0 || cap_ShadowVeryShortTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_ShadowVeryShortTrailingIdx: usize = if cap_ShadowVeryShortTrailingIdx > 0 { cap_ShadowVeryShortTrailingIdx as usize } else { 1 };
        let mut ring_ShadowVeryShortTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inOpen[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inHigh[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inLow[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let mut ring_ShadowVeryShortTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_ShadowVeryShortTrailingIdx];
        ring_ShadowVeryShortTrailingIdx_inClose[..cap_ShadowVeryShortTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_ShadowVeryShortTrailingIdx as usize..]);
        let state = CdlbeltholdStreamState {
            BodyLongPeriodTotal,
            ShadowVeryShortPeriodTotal,
            ringPos_BodyLongTrailingIdx: 0_usize,
            ringCap_BodyLongTrailingIdx: cap_BodyLongTrailingIdx as usize,
            ring_BodyLongTrailingIdx_inOpen,
            ring_BodyLongTrailingIdx_inHigh,
            ring_BodyLongTrailingIdx_inLow,
            ring_BodyLongTrailingIdx_inClose,
            ringPos_ShadowVeryShortTrailingIdx: 0_usize,
            ringCap_ShadowVeryShortTrailingIdx: cap_ShadowVeryShortTrailingIdx as usize,
            ring_ShadowVeryShortTrailingIdx_inOpen,
            ring_ShadowVeryShortTrailingIdx_inHigh,
            ring_ShadowVeryShortTrailingIdx_inLow,
            ring_ShadowVeryShortTrailingIdx_inClose,
        };
        Ok(CdlbeltholdStream { core: self.clone(), state })
    }

}

#[allow(non_snake_case)]
#[allow(unused_variables)]
impl CdlbeltholdStream {
    /// Commit one closed bar; always produces a value. Never allocates.
    #[doc(alias = "TA_CDLBELTHOLD_Update")]
    pub fn update(&mut self, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64) -> i32 {
        let mut outInteger: i32 = 0_i32;
        self.core.cdlbelthold_step_internal(&mut self.state, inOpen, inHigh, inLow, inClose, &mut outInteger);
        outInteger
    }

    /// Evaluate a forming bar without committing — bit-identical to what the
    /// next `update` with the same bar would return (it is the same code, run on
    /// a throwaway clone). Clones the internal state (allocates for windowed
    /// indicators).
    #[doc(alias = "TA_CDLBELTHOLD_Peek")]
    #[must_use]
    pub fn peek(&self, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64) -> i32 {
        let mut scratch = self.clone();
        scratch.update(inOpen, inHigh, inLow, inClose)
    }
}

const _: () = {
    const fn _assert_auto<T: Send + Sync + Clone>() {}
    _assert_auto::<CdlbeltholdStream>();
};

