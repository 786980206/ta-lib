#[allow(unused_variables)]
#[allow(dead_code)]
#[allow(unused_mut)]
#[allow(unused_assignments)]
#[allow(unused_parens)]
impl Core {
    fn cdlhomingpigeon_step_internal(&self, sp: &mut CdlhomingpigeonStreamState, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64, outInteger: &mut i32) {
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let BodyShort_rangeType: i32 = self.candle_settings.body_short.range_type;
        #[allow(non_snake_case)]
        let BodyShort_avgPeriod: i32 = self.candle_settings.body_short.avg_period;
        #[allow(non_snake_case)]
        let BodyShort_factor: f64 = self.candle_settings.body_short.factor;
        sp.ring_BodyLongTrailingIdx_inOpen[sp.ringPos_BodyLongTrailingIdx] = inOpen;
        sp.ring_BodyLongTrailingIdx_inHigh[sp.ringPos_BodyLongTrailingIdx] = inHigh;
        sp.ring_BodyLongTrailingIdx_inLow[sp.ringPos_BodyLongTrailingIdx] = inLow;
        sp.ring_BodyLongTrailingIdx_inClose[sp.ringPos_BodyLongTrailingIdx] = inClose;
        if sp.ringCap_BodyShortTrailingIdx == 0 {
            sp.ring_BodyShortTrailingIdx_inOpen[0] = inOpen;
            sp.ring_BodyShortTrailingIdx_inHigh[0] = inHigh;
            sp.ring_BodyShortTrailingIdx_inLow[0] = inLow;
            sp.ring_BodyShortTrailingIdx_inClose[0] = inClose;
        }
        if ((if sp.lag1_inClose >= sp.lag1_inOpen { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 1st black
           ((if inClose >= inOpen { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 2nd black
           (sp.lag1_inClose - sp.lag1_inOpen).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (sp.BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (sp.lag1_inClose - sp.lag1_inOpen).abs(), 1 => (sp.lag1_inHigh) - (sp.lag1_inLow), _ => (sp.lag1_inHigh) - (sp.lag1_inLow) - ((sp.lag1_inClose) - (sp.lag1_inOpen)).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // 1st long
           (inClose - inOpen).abs() <= ((BodyShort_factor) * (if (BodyShort_avgPeriod) != 0 { (sp.BodyShortPeriodTotal) / (BodyShort_avgPeriod as f64) } else { match BodyShort_rangeType { 0 => (inClose - inOpen).abs(), 1 => (inHigh) - (inLow), _ => (inHigh) - (inLow) - ((inClose) - (inOpen)).abs() } }) / (if (BodyShort_rangeType) == 2 { 2.0 } else { 1.0 })) && // 2nd short
           inOpen < sp.lag1_inOpen &&                                       // 2nd engulfed by 1st
           inClose > sp.lag1_inClose
        {
            (*outInteger) = 100;
        } else {
            (*outInteger) = 0;
        }
        // add the current range and subtract the first range: this is done after the pattern recognition
        // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
        let mut _candlerange_0: f64;
        match BodyLong_rangeType {
            0 => {
                _candlerange_0 = (sp.lag1_inClose - sp.lag1_inOpen).abs();
            }
            1 => {
                _candlerange_0 = sp.lag1_inHigh - sp.lag1_inLow;
            }
            2 => {
                _candlerange_0 = sp.lag1_inHigh - sp.lag1_inLow - (sp.lag1_inClose - sp.lag1_inOpen).abs();
            }
            _ => {
                _candlerange_0 = 0.0;
            }
        }
        let mut _candlerange_1: f64;
        match BodyLong_rangeType {
            0 => {
                _candlerange_1 = (sp.ring_BodyLongTrailingIdx_inClose[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize] - sp.ring_BodyLongTrailingIdx_inOpen[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize]).abs();
            }
            1 => {
                _candlerange_1 = sp.ring_BodyLongTrailingIdx_inHigh[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize] - sp.ring_BodyLongTrailingIdx_inLow[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize];
            }
            2 => {
                _candlerange_1 = sp.ring_BodyLongTrailingIdx_inHigh[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize] - sp.ring_BodyLongTrailingIdx_inLow[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize] - (sp.ring_BodyLongTrailingIdx_inClose[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize] - sp.ring_BodyLongTrailingIdx_inOpen[((sp.ringPos_BodyLongTrailingIdx + sp.ringCap_BodyLongTrailingIdx - sp.ringLag_BodyLongTrailingIdx - 1) % sp.ringCap_BodyLongTrailingIdx) as usize]).abs();
            }
            _ => {
                _candlerange_1 = 0.0;
            }
        }
        sp.BodyLongPeriodTotal += _candlerange_0 - _candlerange_1;
        let mut _candlerange_2: f64;
        match BodyShort_rangeType {
            0 => {
                _candlerange_2 = (inClose - inOpen).abs();
            }
            1 => {
                _candlerange_2 = inHigh - inLow;
            }
            2 => {
                _candlerange_2 = inHigh - inLow - (inClose - inOpen).abs();
            }
            _ => {
                _candlerange_2 = 0.0;
            }
        }
        let mut _candlerange_3: f64;
        match BodyShort_rangeType {
            0 => {
                _candlerange_3 = (sp.ring_BodyShortTrailingIdx_inClose[sp.ringPos_BodyShortTrailingIdx] - sp.ring_BodyShortTrailingIdx_inOpen[sp.ringPos_BodyShortTrailingIdx]).abs();
            }
            1 => {
                _candlerange_3 = sp.ring_BodyShortTrailingIdx_inHigh[sp.ringPos_BodyShortTrailingIdx] - sp.ring_BodyShortTrailingIdx_inLow[sp.ringPos_BodyShortTrailingIdx];
            }
            2 => {
                _candlerange_3 = sp.ring_BodyShortTrailingIdx_inHigh[sp.ringPos_BodyShortTrailingIdx] - sp.ring_BodyShortTrailingIdx_inLow[sp.ringPos_BodyShortTrailingIdx] - (sp.ring_BodyShortTrailingIdx_inClose[sp.ringPos_BodyShortTrailingIdx] - sp.ring_BodyShortTrailingIdx_inOpen[sp.ringPos_BodyShortTrailingIdx]).abs();
            }
            _ => {
                _candlerange_3 = 0.0;
            }
        }
        sp.BodyShortPeriodTotal += _candlerange_2 - _candlerange_3;
        sp.lag1_inOpen = inOpen;
        sp.lag1_inHigh = inHigh;
        sp.lag1_inLow = inLow;
        sp.lag1_inClose = inClose;
        sp.ring_BodyLongTrailingIdx_inOpen[sp.ringPos_BodyLongTrailingIdx] = inOpen;
        sp.ring_BodyLongTrailingIdx_inHigh[sp.ringPos_BodyLongTrailingIdx] = inHigh;
        sp.ring_BodyLongTrailingIdx_inLow[sp.ringPos_BodyLongTrailingIdx] = inLow;
        sp.ring_BodyLongTrailingIdx_inClose[sp.ringPos_BodyLongTrailingIdx] = inClose;
        sp.ringPos_BodyLongTrailingIdx = sp.ringPos_BodyLongTrailingIdx + 1;
        if sp.ringPos_BodyLongTrailingIdx >= sp.ringCap_BodyLongTrailingIdx {
            sp.ringPos_BodyLongTrailingIdx = 0;
        }
        sp.ring_BodyShortTrailingIdx_inOpen[sp.ringPos_BodyShortTrailingIdx] = inOpen;
        sp.ring_BodyShortTrailingIdx_inHigh[sp.ringPos_BodyShortTrailingIdx] = inHigh;
        sp.ring_BodyShortTrailingIdx_inLow[sp.ringPos_BodyShortTrailingIdx] = inLow;
        sp.ring_BodyShortTrailingIdx_inClose[sp.ringPos_BodyShortTrailingIdx] = inClose;
        sp.ringPos_BodyShortTrailingIdx = sp.ringPos_BodyShortTrailingIdx + 1;
        if sp.ringPos_BodyShortTrailingIdx >= sp.ringCap_BodyShortTrailingIdx {
            sp.ringPos_BodyShortTrailingIdx = 0;
        }
    }

    /// Internal startIdx-anchored open behind [`Core::cdlhomingpigeon_open`] (composition seam).
    pub(crate) fn cdlhomingpigeon_open_internal(
        &self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], startIdx: usize,
    ) -> Result<(CdlhomingpigeonStream, i32), RetCode> {
        if inOpen.is_empty() || inHigh.is_empty() || inLow.is_empty() || inClose.is_empty() || inHigh.len() != inOpen.len() || inLow.len() != inOpen.len() || inClose.len() != inOpen.len() {
            return Err(RetCode::BadParam);
        }
        if inOpen.len() > i32::MAX as usize {
            return Err(RetCode::BadParam);
        }
        let historyLen: usize = inOpen.len();
        let endIdx: usize = historyLen - 1;
        let mut startIdx = startIdx;
        let mut dummyBegIdx: usize = 0;
        let mut dummyNBElement: usize = 0;
        let mut lastValue_outInteger: i32 = 0_i32;
        let mut BodyShortPeriodTotal: f64 = 0.0_f64;
        let mut BodyLongPeriodTotal: f64 = 0.0_f64;
        let mut i: usize = 0_usize;
        let mut outIdx: usize = 0_usize;
        let mut BodyShortTrailingIdx: usize = 0_usize;
        let mut BodyLongTrailingIdx: usize = 0_usize;
        let mut lookbackTotal: usize = 0_usize;
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let BodyShort_rangeType: i32 = self.candle_settings.body_short.range_type;
        #[allow(non_snake_case)]
        let BodyShort_avgPeriod: i32 = self.candle_settings.body_short.avg_period;
        #[allow(non_snake_case)]
        let BodyShort_factor: f64 = self.candle_settings.body_short.factor;
        // Identify the minimum number of price bar needed
        // to calculate at least one output.
        lookbackTotal = self.cdlhomingpigeon_lookback();
        // Move up the start index if there is not
        // enough initial data.
        if startIdx < lookbackTotal {
            startIdx = lookbackTotal;
        }
        // Make sure there is still something to evaluate.
        if startIdx > endIdx {
            dummyBegIdx = 0;
            dummyNBElement = 0;
            return Err(RetCode::BadParam);
        }
        // Do the calculation using tight loops.
        // Add-up the initial period, except for the last value.
        BodyLongPeriodTotal = 0.0;
        BodyShortPeriodTotal = 0.0;
        BodyLongTrailingIdx = startIdx - (BodyLong_avgPeriod) as usize;
        BodyShortTrailingIdx = startIdx - (BodyShort_avgPeriod) as usize;
        i = BodyLongTrailingIdx;
        while i < startIdx {
            let mut _candlerange_4: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_4 = (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                1 => {
                    _candlerange_4 = inHigh[i - 1] - inLow[i - 1];
                }
                2 => {
                    _candlerange_4 = inHigh[i - 1] - inLow[i - 1] - (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                _ => {
                    _candlerange_4 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_4;
            i += 1;
        }
        i = BodyShortTrailingIdx;
        while i < startIdx {
            let mut _candlerange_5: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_5 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_5 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_5 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_5 = 0.0;
                }
            }
            BodyShortPeriodTotal += _candlerange_5;
            i += 1;
        }
        i = startIdx;
        // Proceed with the calculation for the requested range.
        // Must have:
        // - first candle: long black candle
        // - second candle: short black real body completely inside the previous day's body
        // The meaning of "short" and "long" is specified with TA_SetCandleSettings
        // outInteger is positive (1 to 100): homing pigeon is always bullish;
        // the user should consider that homing pigeon is significant when it appears in a downtrend,
        // while this function does not consider the trend
        outIdx = 0;
        loop {
            if ((if inClose[i - 1] >= inOpen[i - 1] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 1st black
               ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 2nd black
               (inClose[i - 1] - inOpen[i - 1]).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (inClose[i - 1] - inOpen[i - 1]).abs(), 1 => (inHigh[i - 1]) - (inLow[i - 1]), _ => (inHigh[i - 1]) - (inLow[i - 1]) - ((inClose[i - 1]) - (inOpen[i - 1])).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // 1st long
               (inClose[i] - inOpen[i]).abs() <= ((BodyShort_factor) * (if (BodyShort_avgPeriod) != 0 { (BodyShortPeriodTotal) / (BodyShort_avgPeriod as f64) } else { match BodyShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (BodyShort_rangeType) == 2 { 2.0 } else { 1.0 })) && // 2nd short
               inOpen[i] < inOpen[i - 1] &&                                           // 2nd engulfed by 1st
               inClose[i] > inClose[i - 1]
            {
                lastValue_outInteger = 100;
            } else {
                lastValue_outInteger = 0;
            }
            // add the current range and subtract the first range: this is done after the pattern recognition
            // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
            let mut _candlerange_6: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_6 = (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                1 => {
                    _candlerange_6 = inHigh[i - 1] - inLow[i - 1];
                }
                2 => {
                    _candlerange_6 = inHigh[i - 1] - inLow[i - 1] - (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                _ => {
                    _candlerange_6 = 0.0;
                }
            }
            let mut _candlerange_7: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_7 = (inClose[BodyLongTrailingIdx - 1] - inOpen[BodyLongTrailingIdx - 1]).abs();
                }
                1 => {
                    _candlerange_7 = inHigh[BodyLongTrailingIdx - 1] - inLow[BodyLongTrailingIdx - 1];
                }
                2 => {
                    _candlerange_7 = inHigh[BodyLongTrailingIdx - 1] - inLow[BodyLongTrailingIdx - 1] - (inClose[BodyLongTrailingIdx - 1] - inOpen[BodyLongTrailingIdx - 1]).abs();
                }
                _ => {
                    _candlerange_7 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_6 - _candlerange_7;
            let mut _candlerange_8: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_8 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_8 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_8 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_8 = 0.0;
                }
            }
            let mut _candlerange_9: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_9 = (inClose[BodyShortTrailingIdx] - inOpen[BodyShortTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_9 = inHigh[BodyShortTrailingIdx] - inLow[BodyShortTrailingIdx];
                }
                2 => {
                    _candlerange_9 = inHigh[BodyShortTrailingIdx] - inLow[BodyShortTrailingIdx] - (inClose[BodyShortTrailingIdx] - inOpen[BodyShortTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_9 = 0.0;
                }
            }
            BodyShortPeriodTotal += _candlerange_8 - _candlerange_9;
            i += 1;
            BodyLongTrailingIdx += 1;
            BodyShortTrailingIdx += 1;
            if !(i <= endIdx) { break; }
        }
        // All done. Indicate the output limits and return.
        dummyNBElement = outIdx;
        dummyBegIdx = startIdx;

        // Capture the live batch state into the handle.
        let capLag_BodyLongTrailingIdx: i64 = (i as i64) - (BodyLongTrailingIdx as i64);
        let cap_BodyLongTrailingIdx: i64 = capLag_BodyLongTrailingIdx + 2;
        if capLag_BodyLongTrailingIdx < 0 || cap_BodyLongTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyLongTrailingIdx: usize = if cap_BodyLongTrailingIdx > 0 { cap_BodyLongTrailingIdx as usize } else { 1 };
        let mut ring_BodyLongTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inOpen[fillJ % cap_BodyLongTrailingIdx as usize] = inOpen[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inHigh[fillJ % cap_BodyLongTrailingIdx as usize] = inHigh[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inLow[fillJ % cap_BodyLongTrailingIdx as usize] = inLow[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inClose[fillJ % cap_BodyLongTrailingIdx as usize] = inClose[fillJ];
                fillJ += 1;
            }
        }
        let cap_BodyShortTrailingIdx: i64 = (i as i64) - (BodyShortTrailingIdx as i64);
        if cap_BodyShortTrailingIdx < 0 || cap_BodyShortTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyShortTrailingIdx: usize = if cap_BodyShortTrailingIdx > 0 { cap_BodyShortTrailingIdx as usize } else { 1 };
        let mut ring_BodyShortTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inOpen[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inHigh[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inLow[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inClose[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let state = CdlhomingpigeonStreamState {
            BodyShortPeriodTotal,
            BodyLongPeriodTotal,
            lag1_inOpen: inOpen[historyLen - 1],
            lag1_inHigh: inHigh[historyLen - 1],
            lag1_inLow: inLow[historyLen - 1],
            lag1_inClose: inClose[historyLen - 1],
            ringPos_BodyLongTrailingIdx: historyLen % cap_BodyLongTrailingIdx as usize,
            ringCap_BodyLongTrailingIdx: cap_BodyLongTrailingIdx as usize,
            ringLag_BodyLongTrailingIdx: capLag_BodyLongTrailingIdx as usize,
            ring_BodyLongTrailingIdx_inOpen,
            ring_BodyLongTrailingIdx_inHigh,
            ring_BodyLongTrailingIdx_inLow,
            ring_BodyLongTrailingIdx_inClose,
            ringPos_BodyShortTrailingIdx: 0_usize,
            ringCap_BodyShortTrailingIdx: cap_BodyShortTrailingIdx as usize,
            ring_BodyShortTrailingIdx_inOpen,
            ring_BodyShortTrailingIdx_inHigh,
            ring_BodyShortTrailingIdx_inLow,
            ring_BodyShortTrailingIdx_inClose,
        };
        Ok((CdlhomingpigeonStream { core: self.clone(), state }, lastValue_outInteger))
    }

    /// Open a live CDLHOMINGPIGEON stream over the warm-up history; returns the handle and
    /// the value at the last history bar — bit-identical to [`Core::cdlhomingpigeon`] at that bar.
    ///
    /// # Errors
    ///
    /// [`RetCode::BadParam`] when a parameter is out of range, an input is empty or
    /// input lengths differ, or the history is shorter than `lookback + 1` bars.
    ///
    /// ```
    /// use ta_lib::Core;
    /// let open: Vec<f64> = (0..252).map(|i| 100.0 + 10.0 * (0.1 * i as f64 - 0.05).sin()).collect();
    /// let high: Vec<f64> = (0..252).map(|i| 101.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    /// let low: Vec<f64> = (0..252).map(|i| 99.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    /// let close: Vec<f64> = (0..252).map(|i| 100.0 + 10.0 * (0.1 * i as f64).sin()).collect();
    ///
    /// let core = Core::new();
    /// let (mut s, _last) = core.cdlhomingpigeon_open(&open, &high, &low, &close).expect("enough history");
    /// let peeked = s.peek(100.2, 101.4, 99.1, 100.9);
    /// let updated = s.update(100.2, 101.4, 99.1, 100.9);
    /// assert_eq!(peeked, updated);
    /// ```
    #[doc(alias = "TA_CDLHOMINGPIGEON_Open")]
    pub fn cdlhomingpigeon_open(&self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], ) -> Result<(CdlhomingpigeonStream, i32), RetCode> {
        self.cdlhomingpigeon_open_internal(inOpen, inHigh, inLow, inClose, 0)
    }

    /// [`Core::cdlhomingpigeon_open`] that also fills the output array(s) bit-identically to
    /// [`Core::cdlhomingpigeon`] over `0..len` in the same single pass. Output slices must hold
    /// `len - lookback` values; undersized slices panic (the batch sizing contract).
    #[doc(alias = "TA_CDLHOMINGPIGEON_OpenAndFill")]
    pub fn cdlhomingpigeon_open_and_fill(
        &self, inOpen: &[f64], inHigh: &[f64], inLow: &[f64], inClose: &[f64], outBegIdx: &mut usize, outNBElement: &mut usize, outInteger: &mut [i32],
    ) -> Result<CdlhomingpigeonStream, RetCode> {
        if inOpen.is_empty() || inHigh.is_empty() || inLow.is_empty() || inClose.is_empty() || inHigh.len() != inOpen.len() || inLow.len() != inOpen.len() || inClose.len() != inOpen.len() {
            return Err(RetCode::BadParam);
        }
        if inOpen.len() > i32::MAX as usize {
            return Err(RetCode::BadParam);
        }
        let historyLen: usize = inOpen.len();
        let endIdx: usize = historyLen - 1;
        let mut startIdx: usize = 0;
        let mut dummyBegIdx: usize = 0;
        let mut dummyNBElement: usize = 0;
        let mut BodyShortPeriodTotal: f64 = 0.0_f64;
        let mut BodyLongPeriodTotal: f64 = 0.0_f64;
        let mut i: usize = 0_usize;
        let mut outIdx: usize = 0_usize;
        let mut BodyShortTrailingIdx: usize = 0_usize;
        let mut BodyLongTrailingIdx: usize = 0_usize;
        let mut lookbackTotal: usize = 0_usize;
        #[allow(non_snake_case)]
        let BodyLong_rangeType: i32 = self.candle_settings.body_long.range_type;
        #[allow(non_snake_case)]
        let BodyLong_avgPeriod: i32 = self.candle_settings.body_long.avg_period;
        #[allow(non_snake_case)]
        let BodyLong_factor: f64 = self.candle_settings.body_long.factor;
        #[allow(non_snake_case)]
        let BodyShort_rangeType: i32 = self.candle_settings.body_short.range_type;
        #[allow(non_snake_case)]
        let BodyShort_avgPeriod: i32 = self.candle_settings.body_short.avg_period;
        #[allow(non_snake_case)]
        let BodyShort_factor: f64 = self.candle_settings.body_short.factor;
        // Identify the minimum number of price bar needed
        // to calculate at least one output.
        lookbackTotal = self.cdlhomingpigeon_lookback();
        // Move up the start index if there is not
        // enough initial data.
        if startIdx < lookbackTotal {
            startIdx = lookbackTotal;
        }
        // Make sure there is still something to evaluate.
        if startIdx > endIdx {
            (*outBegIdx) = 0;
            (*outNBElement) = 0;
            return Err(RetCode::BadParam);
        }
        // Do the calculation using tight loops.
        // Add-up the initial period, except for the last value.
        BodyLongPeriodTotal = 0.0;
        BodyShortPeriodTotal = 0.0;
        BodyLongTrailingIdx = startIdx - (BodyLong_avgPeriod) as usize;
        BodyShortTrailingIdx = startIdx - (BodyShort_avgPeriod) as usize;
        i = BodyLongTrailingIdx;
        while i < startIdx {
            let mut _candlerange_10: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_10 = (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                1 => {
                    _candlerange_10 = inHigh[i - 1] - inLow[i - 1];
                }
                2 => {
                    _candlerange_10 = inHigh[i - 1] - inLow[i - 1] - (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                _ => {
                    _candlerange_10 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_10;
            i += 1;
        }
        i = BodyShortTrailingIdx;
        while i < startIdx {
            let mut _candlerange_11: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_11 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_11 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_11 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_11 = 0.0;
                }
            }
            BodyShortPeriodTotal += _candlerange_11;
            i += 1;
        }
        i = startIdx;
        // Proceed with the calculation for the requested range.
        // Must have:
        // - first candle: long black candle
        // - second candle: short black real body completely inside the previous day's body
        // The meaning of "short" and "long" is specified with TA_SetCandleSettings
        // outInteger is positive (1 to 100): homing pigeon is always bullish;
        // the user should consider that homing pigeon is significant when it appears in a downtrend,
        // while this function does not consider the trend
        outIdx = 0;
        loop {
            if ((if inClose[i - 1] >= inOpen[i - 1] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 1st black
               ((if inClose[i] >= inOpen[i] { 1 } else { 0 - 1 })) as i32 == 0 - 1 && // 2nd black
               (inClose[i - 1] - inOpen[i - 1]).abs() > ((BodyLong_factor) * (if (BodyLong_avgPeriod) != 0 { (BodyLongPeriodTotal) / (BodyLong_avgPeriod as f64) } else { match BodyLong_rangeType { 0 => (inClose[i - 1] - inOpen[i - 1]).abs(), 1 => (inHigh[i - 1]) - (inLow[i - 1]), _ => (inHigh[i - 1]) - (inLow[i - 1]) - ((inClose[i - 1]) - (inOpen[i - 1])).abs() } }) / (if (BodyLong_rangeType) == 2 { 2.0 } else { 1.0 })) && // 1st long
               (inClose[i] - inOpen[i]).abs() <= ((BodyShort_factor) * (if (BodyShort_avgPeriod) != 0 { (BodyShortPeriodTotal) / (BodyShort_avgPeriod as f64) } else { match BodyShort_rangeType { 0 => (inClose[i] - inOpen[i]).abs(), 1 => (inHigh[i]) - (inLow[i]), _ => (inHigh[i]) - (inLow[i]) - ((inClose[i]) - (inOpen[i])).abs() } }) / (if (BodyShort_rangeType) == 2 { 2.0 } else { 1.0 })) && // 2nd short
               inOpen[i] < inOpen[i - 1] &&                                           // 2nd engulfed by 1st
               inClose[i] > inClose[i - 1]
            {
                outInteger[outIdx] = 100;
                outIdx += 1;
            } else {
                outInteger[outIdx] = 0;
                outIdx += 1;
            }
            // add the current range and subtract the first range: this is done after the pattern recognition
            // when avgPeriod is not 0, that means "compare with the previous candles" (it excludes the current candle)
            let mut _candlerange_12: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_12 = (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                1 => {
                    _candlerange_12 = inHigh[i - 1] - inLow[i - 1];
                }
                2 => {
                    _candlerange_12 = inHigh[i - 1] - inLow[i - 1] - (inClose[i - 1] - inOpen[i - 1]).abs();
                }
                _ => {
                    _candlerange_12 = 0.0;
                }
            }
            let mut _candlerange_13: f64;
            match BodyLong_rangeType {
                0 => {
                    _candlerange_13 = (inClose[BodyLongTrailingIdx - 1] - inOpen[BodyLongTrailingIdx - 1]).abs();
                }
                1 => {
                    _candlerange_13 = inHigh[BodyLongTrailingIdx - 1] - inLow[BodyLongTrailingIdx - 1];
                }
                2 => {
                    _candlerange_13 = inHigh[BodyLongTrailingIdx - 1] - inLow[BodyLongTrailingIdx - 1] - (inClose[BodyLongTrailingIdx - 1] - inOpen[BodyLongTrailingIdx - 1]).abs();
                }
                _ => {
                    _candlerange_13 = 0.0;
                }
            }
            BodyLongPeriodTotal += _candlerange_12 - _candlerange_13;
            let mut _candlerange_14: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_14 = (inClose[i] - inOpen[i]).abs();
                }
                1 => {
                    _candlerange_14 = inHigh[i] - inLow[i];
                }
                2 => {
                    _candlerange_14 = inHigh[i] - inLow[i] - (inClose[i] - inOpen[i]).abs();
                }
                _ => {
                    _candlerange_14 = 0.0;
                }
            }
            let mut _candlerange_15: f64;
            match BodyShort_rangeType {
                0 => {
                    _candlerange_15 = (inClose[BodyShortTrailingIdx] - inOpen[BodyShortTrailingIdx]).abs();
                }
                1 => {
                    _candlerange_15 = inHigh[BodyShortTrailingIdx] - inLow[BodyShortTrailingIdx];
                }
                2 => {
                    _candlerange_15 = inHigh[BodyShortTrailingIdx] - inLow[BodyShortTrailingIdx] - (inClose[BodyShortTrailingIdx] - inOpen[BodyShortTrailingIdx]).abs();
                }
                _ => {
                    _candlerange_15 = 0.0;
                }
            }
            BodyShortPeriodTotal += _candlerange_14 - _candlerange_15;
            i += 1;
            BodyLongTrailingIdx += 1;
            BodyShortTrailingIdx += 1;
            if !(i <= endIdx) { break; }
        }
        // All done. Indicate the output limits and return.
        (*outNBElement) = outIdx;
        (*outBegIdx) = startIdx;

        // Capture the live batch state into the handle.
        let capLag_BodyLongTrailingIdx: i64 = (i as i64) - (BodyLongTrailingIdx as i64);
        let cap_BodyLongTrailingIdx: i64 = capLag_BodyLongTrailingIdx + 2;
        if capLag_BodyLongTrailingIdx < 0 || cap_BodyLongTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyLongTrailingIdx: usize = if cap_BodyLongTrailingIdx > 0 { cap_BodyLongTrailingIdx as usize } else { 1 };
        let mut ring_BodyLongTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inOpen[fillJ % cap_BodyLongTrailingIdx as usize] = inOpen[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inHigh[fillJ % cap_BodyLongTrailingIdx as usize] = inHigh[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inLow[fillJ % cap_BodyLongTrailingIdx as usize] = inLow[fillJ];
                fillJ += 1;
            }
        }
        let mut ring_BodyLongTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyLongTrailingIdx];
        {
            let mut fillJ: usize = historyLen - cap_BodyLongTrailingIdx as usize;
            while fillJ < historyLen {
                ring_BodyLongTrailingIdx_inClose[fillJ % cap_BodyLongTrailingIdx as usize] = inClose[fillJ];
                fillJ += 1;
            }
        }
        let cap_BodyShortTrailingIdx: i64 = (i as i64) - (BodyShortTrailingIdx as i64);
        if cap_BodyShortTrailingIdx < 0 || cap_BodyShortTrailingIdx > historyLen as i64 {
            return Err(RetCode::InternalError);
        }
        let allocN_BodyShortTrailingIdx: usize = if cap_BodyShortTrailingIdx > 0 { cap_BodyShortTrailingIdx as usize } else { 1 };
        let mut ring_BodyShortTrailingIdx_inOpen: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inOpen[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inOpen[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inHigh: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inHigh[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inHigh[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inLow: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inLow[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inLow[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let mut ring_BodyShortTrailingIdx_inClose: Vec<f64> = vec![0.0_f64; allocN_BodyShortTrailingIdx];
        ring_BodyShortTrailingIdx_inClose[..cap_BodyShortTrailingIdx as usize]
            .copy_from_slice(&inClose[historyLen - cap_BodyShortTrailingIdx as usize..]);
        let state = CdlhomingpigeonStreamState {
            BodyShortPeriodTotal,
            BodyLongPeriodTotal,
            lag1_inOpen: inOpen[historyLen - 1],
            lag1_inHigh: inHigh[historyLen - 1],
            lag1_inLow: inLow[historyLen - 1],
            lag1_inClose: inClose[historyLen - 1],
            ringPos_BodyLongTrailingIdx: historyLen % cap_BodyLongTrailingIdx as usize,
            ringCap_BodyLongTrailingIdx: cap_BodyLongTrailingIdx as usize,
            ringLag_BodyLongTrailingIdx: capLag_BodyLongTrailingIdx as usize,
            ring_BodyLongTrailingIdx_inOpen,
            ring_BodyLongTrailingIdx_inHigh,
            ring_BodyLongTrailingIdx_inLow,
            ring_BodyLongTrailingIdx_inClose,
            ringPos_BodyShortTrailingIdx: 0_usize,
            ringCap_BodyShortTrailingIdx: cap_BodyShortTrailingIdx as usize,
            ring_BodyShortTrailingIdx_inOpen,
            ring_BodyShortTrailingIdx_inHigh,
            ring_BodyShortTrailingIdx_inLow,
            ring_BodyShortTrailingIdx_inClose,
        };
        Ok(CdlhomingpigeonStream { core: self.clone(), state })
    }

}

#[allow(non_snake_case)]
#[allow(unused_variables)]
impl CdlhomingpigeonStream {
    /// Commit one closed bar; always produces a value. Never allocates.
    #[doc(alias = "TA_CDLHOMINGPIGEON_Update")]
    pub fn update(&mut self, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64) -> i32 {
        let mut outInteger: i32 = 0_i32;
        self.core.cdlhomingpigeon_step_internal(&mut self.state, inOpen, inHigh, inLow, inClose, &mut outInteger);
        outInteger
    }

    /// Evaluate a forming bar without committing — bit-identical to what the
    /// next `update` with the same bar would return (it is the same code, run on
    /// a throwaway clone). Clones the internal state (allocates for windowed
    /// indicators).
    #[doc(alias = "TA_CDLHOMINGPIGEON_Peek")]
    #[must_use]
    pub fn peek(&self, inOpen: f64, inHigh: f64, inLow: f64, inClose: f64) -> i32 {
        let mut scratch = self.clone();
        scratch.update(inOpen, inHigh, inLow, inClose)
    }
}

